 * CODIGO SOLUCION PROBLEMA 2 ->  POST TYPE DE LIBROS
 */

 function registrar_libros_cpt() {

    $labels = array(
        'name'                  => _x( 'Libros', 'Post Type General Name', 'text_domain' ),
        'singular_name'         => _x( 'Libro', 'Post Type Singular Name', 'text_domain' ),
        
    );
    $args = array(
        'label'                 => __( 'Libro', 'text_domain' ),
        'labels'                => $labels,
        'supports'              => array( 'title', 'editor', 'thumbnail' ),
        
        'public'                => true,
        'show_ui'               => true,
        'show_in_menu'          => true,
        'menu_icon'             => 'dashicons-book-alt',
        'capability_type'       => 'post',
        
    );
    register_post_type( 'libro', $args );

}
add_action( 'init', 'registrar_libros_cpt', 0 );


/**
 * 
 * Agregar los meta campos
 */

 function libros_add_meta_boxes() {
    add_meta_box('libros_info', 'Información del Libro', 'libros_meta_box_callback', 'libro');
}

function libros_meta_box_callback($post) {
    $autor = get_post_meta($post->ID, '_libro_autor', true);
    $genero = get_post_meta($post->ID, '_libro_genero', true);
    $anio_publicacion = get_post_meta($post->ID, '_libro_anio_publicacion', true);

    // Campos para ingresar la información del libro
    echo '<p><label>Autor:</label> <input type="text" name="_libro_autor" value="' . esc_attr($autor) . '" /></p>';
    echo '<p><label>Género:</label> <input type="text" name="_libro_genero" value="' . esc_attr($genero) . '" /></p>';
    echo '<p><label>Año de Publicación:</label> <input type="text" name="_libro_anio_publicacion" value="' . esc_attr($anio_publicacion) . '" /></p>';
}

add_action('add_meta_boxes', 'libros_add_meta_boxes');

function libros_save_postdata($post_id) {
    if (array_key_exists('_libro_autor', $_POST)) {
        update_post_meta($post_id, '_libro_autor', $_POST['_libro_autor']);
    }
    if (array_key_exists('_libro_genero', $_POST)) {
        update_post_meta($post_id, '_libro_genero', $_POST['_libro_genero']);
    }
    if (array_key_exists('_libro_anio_publicacion', $_POST)) {
        update_post_meta($post_id, '_libro_anio_publicacion', $_POST['_libro_anio_publicacion']);
    }
}

add_action('save_post', 'libros_save_postdata');